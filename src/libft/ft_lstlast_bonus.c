#include "../../includes/libft.h"

/*
Description:
	Returns the last node of the list.
Return value:
	Last node of the list
*/

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
